#include <iostream>
using namespace std;

int main()
{
  // Every statement must finish with a semicolon (;)
  cout << "Hello World!";
  cout << "Have a good day";
  return 0;
}

/*
Each ";" separates statements

In this case, it will execute in the following order:
1 - Print "Hello World" (cout << "Hello World!";)
2 - Print "Have a good day" (cout << "Have a good day";)
3 - Finish the main funcion (return 0;)
*/
