#include <iostream>
using namespace std;

int main()
{
    for (int i = 0; i < 5; i++) {
        cout << i << endl;
    }
    /*
      For loop has three statements within:

      int i = 0 -> Declares variable
      i < 5     -> Sets condition for the loop
      i++       -> Increases the variable

      If the condition at some point is false, it
      will exit the loop.
    */
    return 0;
}
